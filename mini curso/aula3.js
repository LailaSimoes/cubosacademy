//Operadores Aritméticos
let valor = 8;

//Subtração!
console.log(valor - 5);

//Divisão!
console.log(valor / 2);

//Multiplicação!
console.log(valor * 5);

//Soma!
console.log(valor + 10);

//Podemos usar potência também no código!
console.log(valor ** 3);

//Usamos esse operador para saber o resto da divisão!
console.log(valor % 5);

//Podemos juntar textos com operadores, assim juntado-as!
const abc = "Olá";
console.log(abc + "!");