let operacao = "divisao"
let numero1 = 100
let numero2 = 5

if (operacao === "soma") {
  return (numero1 + numero2)
} else if (operacao === "subtracao") {
  return (numero1 - numero2)
} else if (operacao === "multiplicacao") {
  return (numero1 * numero2)
} else if (operacao === "divisao") {
  return (numero1 / numero2)
}
